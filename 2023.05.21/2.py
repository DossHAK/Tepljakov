integer = int(input('Введите любое целое чило: '))
# Введите любое целое чило: 20
print(f'Следующее за числом {integer} число: {integer + 1}')
# Выводим следующее число: 21
print(f'Для числа {integer} предыдущее число: {integer - 1}')
# Выводим предыдущее число: 19


# Результат
# Следующее за числом 20 число: 21
# Для числа 20 предыдущее число: 19
